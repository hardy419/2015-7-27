            <td valign="top" class="section">各位親愛的家長: <br />
                <p>相信你們早前從新聞報道中，獲悉本校學生李芊雪同學於 10 月 24 日(四)上學途中，
                  被一輛公共小巴撞倒入院。感謝上主的憐憫，她現時情況穩定，並由深切治療病房轉
                  到骨科病房接受治療。請各位家長和同學每天為芊雪及其家人禱告，願主親自醫治芊
                  雪及其心靈，讓芊雪得到最好的照顧，並賜芊雪家人平安。</p>
              <p>我與訓導主任黎老師、芊雪班主任郭老師及社工劉姑娘於當日晚上帶同教職員和學
                生的慰問卡到醫院探望芊雪同學，並為她祈禱及作鼓勵。從慰問卡中，我深深感受到
                基愛同學之間的感情有如手足一樣 ;而教師對學生的關切如同父母對子女的關心。基愛
                小學就如一個大家庭，互相支持和關懷。</p>
              <p>從芊雪父母口中得知，芊雪在手術後醒來並未顧及自己的傷勢，竟然第一時間向父
                母查詢妹妹的情況，問:「阿妹有冇事呀?」年紀小小的芊雪竟然有這樣的情操，令人欣
                賞。她並獲悉妹妹並未受傷，便馬上鬆了口氣，完全忘記了自己的傷勢，可見她如此
                愛惜妹妹，令人感動。</p>
              <p>各位同學，我們在芊雪身上學會堅強，她懂得在困苦中學會忍耐。據當日現場途人
                表示，芊雪同學當時沒有發出任何痛苦呻吟，她默默地等待途人的幫助及救護人員的
                救援。</p>
              <p>10 月 28 日當天，我與兩位副校長和班主任再次到醫院探望芊雪，知道她將要進行
                第二次手術，願主保守今次手術成功。另外，非常感謝教育局常任秘書長謝凌潔貞女
                士親自到醫院慰問芊雪，並為她打氣。</p>
              <p>最後，在此懇請各位家長，每天在禱告中為芊雪和她的家人代禱。</p>
