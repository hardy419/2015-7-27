                        <td width="76%"><div class="small_title"><u>黃金夢</u></div><div class="detail_single">
                          一八九零年，一個青年人甘成國隨著他的叔父、帶著掘金的夢想來到美國，他怎樣也想不到神要在這個國家給予他比金子還寶貴之物。一個主日晚上，他走進德薩斯州的霍城（Fort Worth）金巴崙長老會聆聽田朗博士（Dr. R.M. Tinnon）的講道，造次的機會改變了他的一生。甘成國在信主後不久便蒙召，進入金巴崙大學和金巴崙神學院受造就。</div>
                          <div class="small_title"><u>中國！中國！</u></div></td>
                        <td width="24%" valign="top"><p align="center"><a href="http://www.cpcydss.edu.hk/images/gold_010.jpg" target="_blank"><img src="http://www.cpcydss.edu.hk/images/gold_010.jpg" width="116" height="169" border="0"  class="allimg_border"/></a><br />
                          華南金巴崙長老會的開荒者 <br />
                          甘成國牧師</p></td>
                      </tr>
                    </table>
                    <div class="detail_single">
          甘成國仍就讀期間，曾去信本會刊物：<br />
                  我最近閱讀華文報紙得悉中國已漸明白基督教的價值。中國目前已有七十萬基督徒。你們對中國的支持和關注仿照的靈魂，神必然祝福美國，我會在我的同胞中盡力為主工作。
  </div><div class="detail_single">
          甘成國於一八九九年受按立為牧師，最初亦計劃立刻回中國宣教，但總議會卻派他往加州三藩市的華人教會牧會。以後數年，甘牧師分別於恒福（Hanford）、梅錫（Merced）和三藩市的教會事奉，但往中國傳福音的異象卻無時不在他心中。。
  </div><div class="small_title"><u>婦女海外宣教部</u></div><div class="detail_single">
           本會的海外宣教事工，與本會的婦女海外宣教部（Woman’s Board of Foreign Missions）有不可分割的關係。婦女海外宣教部一直是本會海外宣教事工的推動者、策劃者和支持者。
           <p>本會婦女海外宣教部於一八八零年組成。當是由艾雲斯鎮（Evansville）教會的婦女去信呼召本會其他的婦女一同參與這事工：</p>
          
               <p>致金巴崙長老會眾婦女：作為金巴崙長老會的婦女，我們深感日子臨近，我們要在神的預備下，更盡心竭力地投身於拯救在黑暗勢力下的國家的事工。我們已諮詢總議會的意見，現邀請你們派代表出席於一八八零年五月二十五日舉行的會議，以商討組成「婦女海外宣教部」。</p>
             
          
           <p>七十五位婦女出席該次會議，經商討後，婦女海外宣教部正式成立，並與總議會的海外宣教部（Board of Missions）緊密合作，以派宣教士往海外傳福音為使命。</p>
  </div><div class="small_title"><u>西伯利亞號輪船</u></div><div class="detail_single">
           一九零八年夏，甘成國牧師去信婦女海外宣教部，表示他們往中國去傳福音的決心。當時由婦女海外宣教部支持的兩個海外工場正在開展中，總議會亦決定把海外宣教事工完全交由她們負責。但婦女海外宣教部卻沒有足夠的經費與能力支持甘牧師東行。甘牧師當時肯定地回覆婦女海外宣教部：
若你們不能支持我，那末我和家人也決定回中國去。
  </div>
  
  <div class="detail_single">
          甘牧師的決心大大感動婦女海外宣教部的婦女，她們禱告後，決定支持甘牧師回國，向這個當時動盪不安的國家宣講一個平安的福音。就在這期間，甘師母病逝，但甘牧師仍意向堅決，要回國事奉。
  </div>
    <div class="detail_single">
          一九零八年九月二十八日，婦女海外宣教部與甘牧師正式立約，差派他往中國傳道。他早期的經費分別由諾斯鎮宣教會（Knoxville Missionary Society）和肯塔基眾區會（Kentucky Synod）負責。  </div>
  
  
      <div class="detail_single">
          一九零八年十月八日，甘牧師與其養子耀榮一同登上西伯利亞號輪船，往這個他離別十八年的家鄉去。他並不孤單，因為有神與他同行，並許許多多在美國以金錢和禱告支持他的人。  </div>
		  
		  
      <div class="detail_single">
         甘牧師於十一月二十三日抵達香港，再往其家鄉棠下（即新會）。
來到我的家鄉，我只是一個陌生人！我所認識的親友中，除一、兩個外，其他都離世了。但我不孤單，因我知道我的美國朋友為我代禱。我的主也說：「我要與你同在，直到世界末了……」在一個陌生又親切的土地上，甘牧師立於神的應許下，開始他的工作。</div>
  
  
  <div class="small_title"><u>穿西服的中國人</u></div><div class="detail_single">
           甘成國牧師在廣州租得一個小單位，開始他的工作。
               <p>他把地方清理好，放一張桌子在中間，一些雜物，一枝美國國旗，便開始講道。</p>
           <p>但不久他得離開這個地方，因為附近的居民恐怕這個「外國人的神」為他們帶來災害。甘牧師並不灰心，他在昌興街口租得另一個地方，這次的努力看來較為成功，人們漸漸發覺這個穿西服的中國人和他們一樣，也漸漸聽他講道五個鐘頭也不感到煩悶。</p>
           這個小小的聚會點便是廣州大堂的前身，甘牧師勞苦的耕耘，為以後的事工奠下基礎。甘牧師並在此開設學校，為當時適齡的女童提供教育的機會。
