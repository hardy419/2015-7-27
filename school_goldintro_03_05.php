		 <div class="text_title">中國華南區的宣教事工- 金巴崙兒童院</div>
<div class="text_line"></div><div class="detail_single">
  <div align="center"><img src="images/gold_07.jpg" class="allimg_border"/></div>
</div>
<div class="detail_single">金巴崙兒童院位於廣州東山福音中路，當時以港幣$51,209.91購入。院內的建築物包括：「福牧紀念課室」（D.W. Fook’s Hall），多利女子宿舍（Torry’s Girls Dormitory），鍾牧男子宿舍（Jong’s Boys Dormitory），士德頓總務堂（Stockton Administration Building），膳堂和浴室。
兒童院早期的歷史，甘耀敬牧師這樣寫：<br />
本會重辦的兒童教養院，迄今不經不覺又是兩年多了，感謝父神的深恩，在這世界風雲瞬息萬變的兩年裏，力佑我們這大眾的保姆，使他能逐漸的進展者，成為兒童的樂園。但，誰也不會料想到它的前身─兒童保育園，是在第二次世界大戰期中產生的，而又成為了本院的泉源。</div>
<div class="detail_single">一九三九年間，廣州淪陷，無家可歸的孤兒，分散在全省的每一個角落，為了挽救這羣流離失所的羔羊，本會逐商得國際紅十字會的同意，以米麥來供應賑濟各兒童，並借用女青年會為收容地點，另由本會聘請職員來擔教養責任，當時人數六十餘人，我們已是盡了最大的努力，拯救了不少無辜的兒童。這樣的過了兩年，隨著第二次世界大戰爆發，米麥來源缺乏，但我們依舊在艱苦當中盡力支持，直至一九四三年，日軍侵佔了女青年會地址，再沒有辦法，只好把已收容的兒童疏散到江曲內地裏去，然而在我們的腦海裏縈迴，也可以說，這是我們永遠不會忘掉了的事業。</div>
<div class="detail_single">感謝神，在戰爭結束後的一九四六年，我們終也如願以償在東山買了一座房子和五畝多的園地，成立了今日的本院，一九四七年春，便開始收容家貧無依的兒童。為了經費所限，現下收容教養兒童僅三十七人，故甚盼主內兄姊及熱心兒童</div>福利事業人士，能予我們以同情的援助及代禱，這是今後深切的厚望。
<div class="detail_single">當時總會在為兒童院的需要而呼籲奉獻的單張上這樣寫著：<br />
我願意支持一個中國的孤兒―<br />
$120（供給衣食、醫療、學斧和職業訓練一年）<br />
$60支持六個月<br />
$30支持三個月<br />
$10支持一個月</div>
<div class="detail_single">兒童院的設立，正吻合當時中國的需要。甘耀道於一九四六年離開國際救援的工作後，不久便擔任兒童院院長一職，致力發展這有意義的事工。</div>
<div class="detail_single">關於院內的生活和院童的個人見證，簡錄如下：<br />
本院為教會所主辦，故院內一切行政設施，皆以基督為主，對宗教生活極為注重，設有宗教生活極為注重，設有宗教主任負責，每星期日有主日學，並參加本會教會崇拜，每日早晨和晚上，有早晚會及兒童樂園的組織……</div>
<div class="detail_single">本院院址坐落市之東郊，環境優美，空氣清新，有院地五畝，設男女生宿舍、儲物室、課室、職員宿舍、禮堂、辦公樓、圖書館、膳堂等之外，並設有籃球場、排球場、遊戲場等，使學童於課外有正當娛樂，更可鍛鍊強健之體魄，又闢有農作場，種植各種蔬菜及花果。</div>
<div class="detail_single">本院經費來源，大部分由金巴崙長老會集中其屬下各堂之團契及私人教友所捐助，每一兒童由個人或一團契認養，衣、食、住、教育等項費用每名每月約需港幣三十元。</div>
<div class="small_title"><u> 甘耀道</u></div>
<div class="detail_single">我十二歲時就死去了父親，所以我是個孤兒。</div>
<div class="detail_single">我家裏有三個姊姊四個弟弟，和我一共八姊妹。</div>
<div class="detail_single">自從父親死後生活困難，因我母親不能有這麼大能力養育八個孩子，但我母親對於我們抱著莫大的希望，希望我們得救，更為主出力傳播福音。</div>
<div class="detail_single">我的家庭困苦所以難以維持，但感謝主祂愛孤兒祂不丟棄我，感動石灣堂主任余少莊女士介紹我兩個弟弟入了童光院，後來我也得神恩她又介紹我入本院讀書，至於我的三個姊，大的兩個入神學院，一個在家庭工作。我入了本院後，各方面都很好，兄弟姊妹好幾十人，各先生很和藹可親，先生不但注意我們衣、食、住、行、學問等，而且注意我們的靈性，我在院裏像過著樂園的生活，充滿濃厚屬靈的風氣。</div>
<div class="detail_single">感謝主，祂引導我到這個好的環境，可想到主的恩典是何等高闊呢，這真是值得我們稱頌的！</div>
<div class="small_title"><u> 「我的自傅」五年級•潘士諤</u></div>
<div class="detail_single">正當本會各事工在戰後待推進之際，中國掀起巨變，這巨大的改變給中國帶來一個比戰爭還大還深遠的影響，全中國的教會也隨著走上一條艱辛的道路。</div>
