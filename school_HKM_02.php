<div class="text_line"></div><div class="detail_single"><div class="small_title"><div class="detail_single">甘耀道牧師，廣東新會人，一九一六年二月十七日生於廣州。先父甘成國牧師為金巴崙長老會華南區首任宣教士和總幹事。</div>
<div class="detail_single">甘牧師早年在廣州受教育，一九三八年七月赴美深造，五年後完成畢業，於佐治比保地大學取得社會教育碩士學位。回國後，甘牧師即投身於國際救援會的工作，積極從事救饑濟貧的工作。第二次世界大戰結束後，於廣州從事教育工作並兼任本會兒童院院長一職。一九四六年與梁薇丹女士結婚，育有四子一女。</div>
<div class="detail_single">甘牧師於一九五一年被委任為本會中國華南區總幹事，翌年前往美國受接牧職，返港後緻力於發展本會工作，辦學及開展福音工作。先後於澳門、展洲、九龍、北角和梅窩建立教會。香港區會亦於一九八零年成立，甘牧師獲選為首任主蓆。一九六六年獲本會伯特利大學頒授榮譽博士學位。</div>
<div class="detail_single">甘牧師於一九八二年夏退休，與家人定居美國田納西州。一九八八年十二月八日主懷安息，享年七十四歲。</div>
<div class="detail_single">甘牧師一生投身本會華南區事工，他是開荒者、領導者、也是教育家。甘牧師為人嚴謹，做事絲毫不苟，卻極具幽默感。他對提攜後進可算盡心竭力。對會友，是亦父亦友。他的一生嚮我們顯示對神委身、獻身教會事奉的榜樣。</div>近年來雖然缺乏同工，但神興起祂的兒女積極發展教會的工作，並有黃誌強牧師、師母和王其昌牧師於不同的時間內，擔任堂會的顧問牧師。</div>
