		<div class="text_title">金巴崙長老會港澳的宣教事工- 北角堂</div>
<div class="text_line"></div><div class="detail_single"><div class="small_title"><div class="small_title"><u>港北之角</u></div>
<div class="detail_single">五零年代之北角只是香港島北面一小角，本會在此發展第三間教會。正如長洲堂和九龍堂一樣,堂會初期的發展以牧養本會由中國遷居香港的會友為主，同時亦積極向居民傳福音。而早期因教牧同工人手不足夠，當時的同工均分擔各堂會的工作。早上在北角堂負責崇拜，下午則在九龍堂崇拜。
北角堂於一九五九年八月四日舉行新堂獻堂禮。初期聚會人數約三十人，大部分會友是由中國遷居香港之會友，亦希不少是附近的居民。當時宋愛倫姑娘由澳門調回北角堂事奉，而萬灼明先生則協助主日學、兒童和探訪工作，並兼任石硤尾金巴崙小學及長洲識字班導師。</div>
<div class="small_title"><u>早期事工</u></div>
<div class="detail_single">六零年代，聚會人數增至四十多人。會眾以家庭成員為單位者眾多，不少會友均來自鄰近之屋邨。</div>
<div class="detail_single">這時期同工變動大，在堂會事奉的期間也短至一二年，堂會缺乏一個長期領導的同工。在這期間分別有范綺芳姑娘、趙偉彬先生，溫仙圓姑娘、方綺蓮姑娘和梁建珍姑娘在北角堂服事。</div>
<div class="small_title"><u>穩步向前</u></div>
<div class="detail_single">自一九七一年始，萬灼明先生辭去金巴崙小學之教職，全職負責堂會之事奉，並同時在建道和播道兩間神學院進修。梁小甜姑娘於一九七五年任區會同工期間，與夫婿余達心先生積極參與堂會之工作。</div>
<div class="detail_single">早期教會之兒童已成長為青年人，堂會中成年人和老年人也不少，但會眾仍保留在五十人左右，其一的原因是不少早期會友移民外地。堂會於一九七四年經濟自立，並於兩年後按立黃景褆和高鴻鈞兩位為長老，於七七年一月一日正式成立理事會。當時有不少青年信主加入教會，婦女工作也蒙神祝福，教會穩步向前發展。七八年曹阮貞女士（曹恩保師母）任女傳道，幫助探訪和婦女工作。</div>
<div class="detail_single">一九八二年是另一個轉變期。甘耀道牧師正式退休，與師母於同年三月移居美國田納西州。甘牧師雖任區會總幹事，但一家人均積極參與北角堂之事工，甘師母亦負責詩班和婦女工作。堂會決定把甘牧師舊居改作閱覽室，供學生課余溫習自修之用，並藉此接觸未信主的學生。林月愛姑娘亦於同年八月任職北角堂。</div>
<div class="detail_single">八二年一月按立萬灼明先生和余達心先生為牧師。余達心牧師任堂會之顧問牧師。</div>
<div class="small_title"><u>新的堂址</u></div>
<div class="detail_single">由八零年到九零年，堂會大概脫離了往日單以家庭為主的會友組成單位，不少人加入教會是經濟參加團契或查經班。當時聚會人數約五十人，惟地方不夠使用，故於一九八三年初遷往七姊妹道新堂聚會，並於二月二十八日舉行獻堂禮，由甘耀道牧師、師母返港主持典禮。</div>
<div class="detail_single">新堂開放作閱覽室，供中學生溫習，堂會亦計劃對內訓練信徒領袖，對外接觸街坊。新的地方，同時也帶給堂會新的挑戰。游玉蘭姑娘於一九八五年任宣教師，帶領青年工作和教導事工，她的夫婿徐正文先生任義務同工，協助教導事工。在以後數年，堂會的同工亦有不少變動。余牧師、師母於八五年調往發展另一新基址─道顯堂，而游姑娘亦於八八年底調往另一基址―沙田堂。萬灼明牧師在事奉多年後，於九零年十一月底離職。堂會缺乏同工一段時間後，黃慕聖牧師於一九一年八月調任，事奉至九三年三月退休止。余梁小甜女士於九三年夏自美歸港後，於年底任北角堂顧問，協助堂會工作。</div>
<div class="detail_single">三十七年來，堂會人事數度變遷。港北之角，仍是神叫教會守望之所。</div></div>
