            <td valign="top" class="section">各位親愛的家長: <br />
                <p>感謝天父，基愛小學開學至今各樣的工作都順暢進行，在此感謝老師們的辛勞。 </p>
              <p>每天早上，我都會與兩位基愛小學禮貌大使一同在學校門外迎接學生回校上課。看見每一
                位學生不論是自行背著書包回校，還是由父母帶着回校，他們每一個都是笑容可掬的，從他們
                既親切又燦爛的笑容可知道，學生喜愛基愛這個大家庭！ </p>
              <p><img src="images/word_08.jpg" width="522" height="261" /></p>
              <p>上個月其中兩個周六的下午，學校為區內幼稚園家長舉辦了家長教育講座，參加人數多達
                200 人，家長如此踴躍參加，表示他們對子女教育的重視及對基愛的信任及支持。 </p>
              <p>家長講座的講題包括:｢優秀是訓練出來」、｢高效識字法」、｢高效拼音 ABC」、｢快快樂樂學
                數學」。其中由本人所講的｢優秀是訓練出來」講座，家長都十分欣賞及主動發問，因為他們都
                關心如何令孩子可以聽教一點，以及了解教養孩童的方法。現今，大部分家庭都是以一個小朋
                友為主，很少父母會擁有三個以上的孩子。平日所見，大部分的孩子都較為自我中心、我行我
                素和很少顧及別人的感受。有時候，當你經過超級市場或玩具店，經常看到小朋友在哭鬧，嚷
                著父母要購買他們所喜愛的東西，父母很多時都因為怕他們當街叫嚷而就範，順從孩子的任性
                要求。其實，我們可以從小訓練他們的意志和忍耐。在講座中，我經常提到父母必須從小訓練
                孩子的意志。優秀的孩子就是要有堅定的意志。甚麼是意志? 意志的決定是取決於孩子的價值
                觀。舉個例子:孩子每天都沈迷看電視，不願做功課，作為家長可以怎樣做呢?其實，父母可先
                與孩子共同建立每天看電視的時間。例如，每天只可以看一套卡通片或以小時為限。當他們不
                依從，你便可以跟他們表示，如果今天多看了 20 分鐘，明天便只得 40 分鐘看電視的時間，讓
                孩子自行抉擇，這就是意志的選擇。 </p>
              <p>各位家長，就由今天起讓孩子學習意志的選擇，這對他們日後的學習或做人處世都有莫大
                的裨益。 </p>
              <p>最後，在此祝福各位基愛的家長主恩常在，身心愉快，讓我們一起見證天父的帶領。 </p>
