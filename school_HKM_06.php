	  <div class="text_title">金巴崙長老會港澳的宣教事工- 道顯堂 </div>
<div class="text_line"></div>
<div class="detail_single">
	<div class="small_title"><u>點燃一個夢</u></div>
</div>
<div class="detail_single">道顯堂的成立始於一個偶遇；突破機構的蔡元雲醫生一天致電余達心牧師：「突破考慮購買吳松街一幢十四層的大廈，我們希望其中一層為教會，以便跟進福音工作，你們教會會考慮？」這個計劃既不在本會拓展工作計劃內，當時亦沒有足夠的人力、財力，但事情就是這樣實現了。當時蔡醫生邀請本會同走「信心之旅」，與突破合作做好讀者和青少年跟進工作。雖然在某程度上，這個合作一直以來不十分合拍，但神卻向我們展示祂的祝福。
</div>
<div class="detail_single">當時的突破中心十二樓面積約二千呎，樓頜一百零四萬。一九八五年十月十三日舉行首次主日崇拜，同年十月二十七日下午二時舉行獻堂禮。當時的「核心成員」包括：在余牧師家中查經班的成員、浸會學院的學生和北角堂的賴慰庭長老等。當時一班弟兄姊妹對教會有一個夢想，就是建立教會成為一個敬拜的羣體、服事的羣體、醫治的羣體和見證的羣體，這「四項堅持」也成為堂會以後的方向。
</div>
<div class="detail_single">
新堂由余達心牧師為義務堂主任，陳再盛先生為宣教師。開堂初期崇拜人數約五十人。
</div>
<div class="small_title"><u>開墾與耕種</u></div>
<div class="detail_single">堂會的事工一直重視屬靈生命的內程和外程；對內重整生命，對外接觸和服事人羣。早期的事工向外的接觸面大；例如舉辦社區嘉年華會、服事區內老人和舉辦興趣小組等。另一方面，發展基層人士的事工也是堂會另一堅持。星期六晚上的崇拜聚會於一九八六年十一月一日首次舉行，赴會者多為基層人士，以後晚堂的事工也迅速發展。
</div>
<div class="detail_single">早期會聚以青年人為主，其後少年工作亦繼續而展開。約於二年後，崇拜人數已有百多人，當時地方不敷之應用，有一段時期，主日學假借鄰近的學校上課，最擠迫時，連樓梯轉角位也僻作課室。
</div>
<div class="detail_single">早期的同工除余牧師和陳再盛先生外，分別有林德明先生、張愛玲姑娘、區建銘先生加入，可惜他們的服事也以一二年為期。楊建強先生於一九八九年加入事奉隊工，陳再盛先生則於九零年離職。孫遜萍小姐曾為部分時間同工，負責輔導工作和帶領成長小組。
</div>
<div class="detail_single">因崇拜人數快速增加，十二樓的堂址已不夠應付。八九年一月開始租用突破中心一樓禮堂舉行崇拜，當時人數已達二百人。會眾大部分是大專畢業生，其中教師、社工、醫生、行政人員等佔多數，漸漸晚堂聚會除基層人士外，亦有不少專業人士參加。在道顯堂土生土長第一代的青年人也漸長成，加上有不少經常來聚會的訪客，形成一個龐大牧養的需要。</div>
<div class="detail_single">青年團契於八八年成立、八九年成立包括教牧人員和信徒的「牧養隊工」，九零年初始「Quality Building Ministry」，用小組方式以建立靈命進深的學習，而其他的工作如「宣教事工」、向家人傳福音的工作亦繼續開始。關懷、栽培和聯絡成了當時事工的中心，以穩定、凝聚、關顧當時流動性頗大的會眾。這段時間內，加上同工的流動，是一段辛勤耕耘的日子。
</div>
<div class="small_title"><u>風雲年代</u></div>
<div class="detail_single">九零年代初期又是另一個轉變階段。道顯的第一代不少已經大學畢業，投入為會工作，堂會亦漸漸有很多年青夫婦和年青家庭，經過過往幾年的凝聚，會眾的流動性較少，而且對教會的歸屬感增多。九一年按立余俊豪弟兄為長老，同年余達心牧師往美國進修兩年，張略牧師於九一年底任義務主任牧師。
</div>
<div class="detail_single">堂會於九二年底購得廟街一個四千平方呎的單立，並於同年十一月舉行獻堂禮。新堂較舊堂寬大一倍多，具備多個辦公室、小組房和副堂。因崇拜人數已達二百多人，故除晚堂崇拜外，主日分兩堂崇拜。張慧玲姑娘、鄭翠蓮姑娘和劉正文先生也分別於不同時間內加入同工隊伍。楊建強先生於一九九五年九月十日受按立為牧師聖職。
</div>
<div class="detail_single">九零年代的事工方向與堂會現況、香港社會的劇變有著密切的關係。道顯堂一方面繼續「四個堅持」：道顯是一個敬拜、服事、醫治和見證的羣體，另一方面，在面對九七問題所湧現的種種社會現象，堂會在息息不斷的宣講、徹養、訓練和教導的職事中，更要面對時代的挑戰。
</div>   
