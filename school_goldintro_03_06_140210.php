		 <div class="text_title">中國華南區的宣教事工- 別矣吾土</div>
          <div class="back_page"><a href="school_goldintro.php" target="_self">返回上一頁>></a></div>
<div class="text_line"></div><div class="detail_single">
  <div align="center"><img src="images/gold_08.jpg" class="allimg_border"/></div>
</div><div class="detail_single">中華人民共和國於一四九年十月一日正式成立，中國遂走上一條與過去完全不同的道路。</div>
<div class="detail_single">根據統計顯示，在新中國成立之時，全中國約有二千名基督教的宣教士，教會產業總值二千七百萬美元，全美各宗派的教會在該年內為中國宣教事工的奉獻達八百萬美元。基督教會在全國共辦有十三間大學，數以百計的小學和中學，超過二百間醫院和無數診所，各種社會服務等。教會在中國的工作並未立刻終止，但延至五零、五一年時，反外的形勢已十分明顯，當時已不容許任何宣教士入境而留在中國的教牧傳道和宣教士，紛紛遭受各種不同的壓力，延至五一年底，差不多所有的宣教士在迫不得已的情況下離開中國，他們當中不少人便取道往東南亞的工場事奉。</div>
<div class="detail_single">甘耀敬牧肩於一九四九年底舉家赴澳門後，便不得再進入中國的工場。甘耀敬牧師因當時並未受按立，仍可自由進出中國，但到五一年，也逼得遷往澳門。本會只得幾位教牧同工仍留守教會，其中包括廣大堂的甘建磐牧師。</div>
<div class="detail_single">本會從一九四九年的年報顯示，當時廣大堂有會友423名，沙溪堂會友共350名，兒童院正計劃擴展，以收容更多無助中的孤兒。本會在華南區的工作經過四十多年的艱苦耕耘，在整個政治局面改變時，本會的教會於五零年初期逐漸被逼停止聚會。不少會友親眼見教會物業被毀，聖經、詩歌書被焚，甘建磐牧師和其他同工接受勞改、再教育，本會所有的教會已停止聚會。
神帶走祂的工人━教牧、宣教士，但神仍留下一羣祂忠心的兒女默默地在工作，神繼續祂的工作。</div>
<div class="small_title"><u>後記</u></div>
<div class="detail_single">一九八八年四月，本會在香港的部分同工踏上廣州路，為的是追尋昔日本會各教會的蹤跡。我們盼望知道：昔日的堂址仍在嗎？會友們怎樣了？以下部分資料由阮永福牧師和其夫人潘琼珍牧師提供。<br />
本會於解放前的廣州市有三間教會：<br />
廣大堂的堂址已拆毀。現址為一間幾層高的商業公司，另一半堂址現為市場的一部分。</div>
<div class="detail_single">東漖堂位於廣州郊區，辦有東漖小學，黃逸民牧師曾任主任。當漖堂約於一九五一年關閉。</div>
<div class="detail_single">河南堂地址位於龍導尾（龍尾導）將軍直九號，往訪時舊堂址仍存。解放前有會友百多人、主任牧師為梁偉生牧師。梁牧師夫婦仍健在，住在教會二樓。目前的堂址已轉為一文化店，租予當地辦識字班和閱覽室。政府的計劃是以後改建為老人院。</div>
<div class="small_title"><u> 中山市三堂：</u></div>
<div class="detail_single">沙溪堂的舊址已改建為一製衣工場。本會會友於五一年教會關閉後仍在沙溪市其他的信徒一起暗中聚會，他們表示「聚會從未停止過」。到八零年代初期，由於政府政策改變，仿照便開始要求政府撥還本會在市墟的一個鋪位，並在該址聚會至八四年左右，才由政府正式撥地興建一間教會，名隆都堂（隆都乃沙溪古名）。目前在隆都堂聚會的人不少為本會昔日沙溪堂會友。
大環堂和可岐堂原址不存。</div>
<div class="small_title"><u> 另外的堂會情形：</u></div>
<div class="detail_single">佛山古洞堂位於佛山古洞直街。於文革時，部分堂址被毀。</div>
<div class="small_title"><u> 石灣堂堂址不存。</u></div>
<div class="small_title"><u> 松江福音堂於解放後不久便結束。</u></div>
<div class="small_title"><u> 兒童教養院目前為一小學。</u></div>
<div class="detail_single">廣州市銀河公墓內的一側為基督徒安葬的地方，本會和其他各教會，信徒的墓地均在此。本會昔日不少的信徒與他們所愛的牧師，包括甘成國牧師和甘建磐牧師均在此安睡，等待號角吹響的那個晨早。</div>
