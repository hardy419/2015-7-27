<div class="text_line"></div><div class="detail_single">
  <div align="center"><img src="images/gold_09.jpg" class="allimg_border"/></div>
</div><div class="detail_single"><div class="small_title"><div class="small_title"><div class="detail_single">港澳區的工作，自一九五零年開始後，經歷各階段的轉變，在組織上亦因而有不同的發展。</div>
<div class="detail_single">早於五零年代，四間堂會剛開始工作，港澳區的事工由「長老、執事、同工聯席會」策劃和推動。這個聯席會議每年開會約三四次，共同商討各堂事工。於五九年十二月，選出十部門，由不同的人負責。</div>
<div class="detail_single">一九六九年八月，組成「港澳區工作委員會」。基本上亦由各堂同工、長老、執事、信徒領袖組成，設十個部門，由不同人選擔任工作。當時各堂關係密切，聯合性的聚會經常舉行，且每年均舉行聯堂夏令退修會，上午聽道，下午議事。至七零年初則改為上午參加港九培靈大會，下午回教會議事。
本會自中國南遷到香港後，甘耀道牧師一直是本會的總幹事，負起領導的責任。</div><div class="detail_single">其後各堂亦分別按立長老和執事，一同承擔主的託負，建立神家。七零年中期，各堂會事工有不少突破性發展；北角堂和九龍堂邁向經濟自立之途，且組成理事會，成為自立的堂會。自七六年始，也分別由溫天羽長老、黎恩長老任「港澳區十部門」的主席。</div>
<div class="detail_single">金巴崙長老會香港區會於一九八零年五月四日成立，標誌著另一個新里程。甘耀道牧師獲選擔任主席，本會在香港的工作踏入新階段，由信徒領袖和教牧人員一起負責聖工，在行政、經濟和組織上脫離以美國總會為主導的形式，由區會成員承擔領導的責任。</div>
<div class="detail_single">甘耀道牧師於一九八二年初退休。美國總會於同年一月聘任余梁小甜女士為香港區總幹事（Mission Co-ordinator）與香港區會成員一同事奉。余梁小甜女士於一九九一年至九三年離港期間，由張梁笑媚女士擔任署理總幹事。余梁小甜女士於一九九三年九月復任，並於一九九五年九月十日受按牧職。一九九五年一月，區會聘任前沙田同工徐游玉蘭女士區會行政秘書。</div>
<div class="detail_single">本會美國會友白蘭西小姐（Miss Nancy Boecker）於一九九零年六月由其母會支持，並由本會普世宣教部差派在本區事奉。白小姐除學習語言外，更負責策劃堂會之英語補習課程和英語營，並任教於寶林幼稚園和積極參與沙田堂的事奉。白小姐于一九九六年七月離任。</div></div>
