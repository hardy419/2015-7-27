		<div class="text_title">金巴崙長老會港澳的宣教事工- 寶林堂及寶林幼稚園</div>
<div class="text_line"></div><div class="detail_single">
  <div align="center"><img src="images/gold_02.jpg" class="allimg_border"/></div>
</div><div class="small_title"><u>意外的驚喜</u></div>
<div class="detail_single">寶林堂的開始與寶林幼稚園有相連相關的關係。區會於一九八五年一月向政府申辦小學，於一九八七年四月申辦幼稚園，正因不欲受製於難以預測之輪值時間，而決定於沙田植堂數月後，一九八九年一月，獲政府批准於將軍澳寶林邨開辦幼稚園。</div>
<div class="detail_single">幼稚園於一九八九年九月開課。院內面積約八千平方呎，分三級製共有學生四百余人。我們在將軍澳的服務帶著一個信念，就是開辦一間具質素的幼稚園和建立一個植根於所處為區的信仰羣體─教會。因幼稚園的開辦來得比較突然，教會的開展也有點亂了陣腳。神預備全球福音傳揚會的林永康先生負起植堂的責任，植堂小組中亦加上楊建強先生和余梁小甜女士。九零年八月陳寶虹姑娘加入同工行列，負責堂會工作並幼稚園宗教教育事工，加上道顯堂縛位青年人協助堂會的青年工作，這隊伍便成為寶林堂的同工。</div>
<div class="small_title"><u>獨特的羣體</u></div>
<div class="detail_single">寶林堂於九零年三月十一日舉行首次主日崇拜，當時聚會者全是居於區內之居民。堂會事奉對象以婦女和年青人為主，堂會以後一直的發展，也是服事區內居民為主。將軍澳區居民大部分為過去十年內移居香港之新移民，有其獨特的文化特色。婦女的事奉除恆常的教導、佈道事工外，家庭生活的教導和輔導工作也很重要。青年人的事工亦蒙神祝福，自九零年於暑假開辦暑期英語營後，以後每年均有舉辦，不少青年人因而接觸教會，引發對福音的興趣。</div>
<div class="detail_single">屈綺華姑娘於一九九二年九月由全球福音傳揚會委派到本堂參與植堂工作。陳寶虹姑娘則於一九九二年九月始擔任幼稚園之宗教教育主任並協助堂會工作。</div>
<div class="detail_single">一九九三年七月，租得附近一村屋地下作為教會辦事處並小組活動之用，二年後租約期滿停止租用。堂會計劃另覓合適地方發展為教會中心，以便更能接觸和服事社羣。於九六年初，神預備一個合適的地方，以致教會能進一步服事社羣。教會於慧安商場開辦「將軍澳牧鄰中心」，同年五月投入服務。中心除開辦各種不同的補習班、興趣小組和活動外，並設有小書室，中心成為教會接觸容服事人群的地方。</div>
<div class="small_title"><u>任重道遠</u></div>
<div class="detail_single">神為寶林幼稚園預備一位由開校至今肩負責任的羅翠玲校長，並一羣忠心的老師，我們的關注、服事的對象不單是學生，更希望藉著親子關係的教育和活動，可以建立家長的家庭生活。本會數位會友和余師母擔任幼稚園校董，他們是郭蔡珊珊姊妹、何姚玉雲姊妹告賴慰庭長老，另外鄭楊月堅女士、余達心牧師、張梁笑媚姊妹亦曾為校董會成員。</div>
<div class="detail_single">幼兒教育和教會事奉的配合和關連是我們要不斷探索的問題。過去六年，教會和幼稚園均接觸到很多人，他們當中有肉身貧乏、生命貧乏的。遙望近調景嶺的填海區，以後約共有四十萬人住在將軍澳區，堂會仍需建立鞏固的屬靈根基，好為神打美好的仗。</div>
