            <p align="center"><strong>再思耀中精神</strong><strong> </strong></p>
            <p>            親愛的家長： </p>
            <p>　　二十一世紀是一個資訊爆炸、全球一體化、充滿挑戰和機遇的年代。作為教育工作者，我時常思想，耀道中學如何有效地準備學生迎接這個新世代。投身教育工作接近三十年，無論在不同學校的經驗，都告訴我一個不變的教育法則：孩子的學習潛能在一個高期望、受尊重及受信任的環境中必能發揮得最好。 </p>
            <p>　　金巴崙長老會耀道中學秉承基督教的教育信念，為學生提供優質及均衡的教育。我們的校訓是｢尚卓越鏈剛毅熱切求真    效基督學捨己榮神愛人｣。作為一所基督教學校，金巴崙長老會耀道中學相信每個孩子被  上帝創造時都已經擁有不能被取代的尊貴價值，教育的過程是學校和老師利用不同的平台、安排不同的學習活動，營造不同的氛圍，讓學生參與其中，發揮潛能。老師的責任在於創造一個充滿尊重、支持、信任及富挑戰性的環境，讓每一個學生的才幹都能得到充分的發展和肯定。我認為這種風氣代表人文教育的價值，人文教育的真髓並不在於特地去強調文哲史的修養，而在於培育個人自我的內在價值及對人生的態度，從而提高生命的質素及內涵。 </p>
            <p>　　社會需要的能力會隨著時代變遷而不斷演變。經濟架構的變化，科技領域的興衰，新興技術的發明會引發對於新的專業能力及倫理品德的需求。為幫助學生面對未來的挑戰，元朗耀中在辦學之初，已經訂立了以下的六大方向： <br />
              　　(1) 建立學生中英文的溝通能力。 <br />
              　　(2) 培養學生創意思維，解難思考的能力。 <br />
              　　(3) 培訓學生不同的專業技能及興趣。 <br />
              　　(4) 培養學生了解國情、開拓學生國際視野。 <br />
              　　(5) 建立學生自我管理的能力，面對困難的堅忍力。 <br />
              　　(6) 幫助學生內化自由、平等、互相尊重、感通相愛與自我超越的人文精神價值。 </p>
            <p>　　親愛的家長，我們深深感受到這些年間神的恩典眷佑。我們對學生的成長感到快樂和喜悅，我們深愛他們，期望在全體老師和家長的通力合作下，能夠培育出一批懂得看重自己、肯定自己、關懷別人、具正確價值觀、勇於對面挑戰、樂於承擔責任和積極面對人生的學生。
			</p>
