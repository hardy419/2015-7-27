<div class="text_line"></div><div class="detail_single">
  <div align="center"><img src="images/gold_04.jpg" class="allimg_border"/></div>
</div><div class="small_title"><u>八個福音基址</u></div>
<div class="detail_single">甘牧師繼續在廣東省各地區展開工作，在香山縣（即中山市）的沙溪，他開始向村民傳講福音，村民的反應有冷淡的、也有好奇的，但他並不灰心，直到一九一二年，正式在沙溪設立基址。沙溪一地的工作蒙神祝福，四年之後，教會增長很快，教會亦開辦分別為男女童而設的學校。</div>
<div class="detail_single">第三間基址於一九一四年在廣州市的河南開始。日後甘牧師提到河南堂的工作時，他表示：我們有一約七十五位會友，一間男校，有學生八十五人，而且教會的閱讀室由早上九時至下午四時開放為村民服務。</div>
<div class="detail_single">第四間基址於一九一五年於大環開始。大環堂的建堂費大部分由三藩市華人金巴崙長老會資助，共費4,500美元，獻堂禮於一九一六年一月九日舉行。堂址樓高兩層，可容四百人聚會，二樓用作學校。</div>
<div class="detail_single">於沙溪附近的大中，甘牧師開始第五個基址，如以往的情形一樣，教會亦辦識字班，以解決當時的教育問題。</div>
<div class="detail_single">本會第六個福音基址是一個專向苦力傳福音的據點，當時在廣州市約有一千個苦力，他們生活艱辛，乏人關心，經常患病，甘牧師在他們中間不單宣揚福音，亦關心他們實際的需要。約有八十人先後受洗加入教會。</div>
<div class="detail_single">東漖堂於一九一七年成立，當時由一位信徒受感動捐出一塊地用以建堂。當時的禮堂可容二百人聚會，學校則設在二樓。</div>
<div class="detail_single">甘牧師回國十年，在廣東省並建立八個基址。論到他忠心的耕耘，本會一位婦女領袖奇麗太太（Mrs. Johnie Massey Glay）曾這樣說：</div>
<div class="detail_single">神帥領他忠心的僕人不斷前進。甘牧師具堅決的心志、不竭的精神，他是堅忍勝過一切，他對神的信心叫他得勝。</div>


<div class="small_title"><u>首次回國述職</u></div>
<div class="detail_single">一九二零年，甘牧師回到美國，與一群支持他的人相聚。當時在廣州的幾間基址仍在發展中，廣大堂的地方已不足應付各種活動之用，甘牧師希望美國的朋友能奉獻支持改建新堂。總議會決定設立特別奉獻，為三藩市金巴崙長老會和廣大堂建新堂而奉獻。當時的口號是：「若我們意定，必能於一九二零年建兩間教堂。」</div>
<div class="detail_single">於二零至二一年內，共籌得36,000美金。廣大堂於一九二二年建成。</div>
<div class="small_title"><u>動盪不安的中國</u></div>
<div class="detail_single">自孫中山先生推翻滿清，建立民國以來，中國一直陷於內亂中。國佩軍閥掌權割據，國民黨與共產黨兩黨之爭，令中國政局混亂不安。</div>
<div class="detail_single">在甘牧師寫於一九二三年的書信中，可較為了解政治局面的變動對教會的影響。
……去年夏天，我也曾於路上被土匪洗劫一空，最怕人的是仿照還想標參，神實在保守我脫離這災難。……本會各教會均曾遭劫掠，政府根本難以控制大局，整個局面很不穩定，有些地方一年內由不同的軍人統治。……廣東省亦被一隊軍佔領，軍人通常會扎該地搶掠一空。廣大堂的會友發起一星期的禱告會，為廣州城禱告，結果軍隊竟然沒有造成任何擾亂。</div>
<div class="small_title"><u>組成廣州區會</u></div>
<div class="detail_single">在動盪不安的政局中，本會的廣州區會於一九二四年十月二十四日組成。當時總議會委派福牧師（Rev. D.W. Fooks）來華，與甘牧師共同籌備區會的組成，宣道差會（Alliance Mission）的梁永根牧師（譯音）和美國瑞士會的胡石興牧師（譯音）加什為同工，當時本會的教牧同工除他們以外還有：何貴德牧師（譯音）和盧同福牧師（譯音），三位女傳道。當時本會共有七間堂會，會友1,060名，二十五至三十位教師在各學校任教，學生人數共七百至八百名。</div>
