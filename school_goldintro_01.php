          <div class="small_title"><u>復興之火</u></div><div class="detail_single">
            <p>本會在歷史始源於十八世紀末在美國一連串的復興運動。聖靈的火先在學生運動中點燃，不少人以後加入事奉行列，在長老會的教會中事奉。約於一七九六年，美國長老會的一位牧師占士‧麥紀德（Rev. James McGready）首先得著個人復興。他傳講「重均、信心和悔改」的信息，並鼓勵信徒與他彼此立約：「以一年為期，它每月的第三個星期六為禁食、禱告的日子，為盧根郡（Logan County）和全世界的罪人禱告。」當時在盧根郡的幾間長老會都經歷極大的祝福。復興的火一直蔓延，至一八零零年，整個金巴崙郡的長老會都經歷聖靈的大能；信徒步行幾十哩至一百哩路來參加帳棚培靈佈道會，信主者大大增加。 </p>
            <p> 金巴崙郡因金巴崙山而命名，約指現在肯塔基州和田納西州交界的地域。 </p>
  </div>
          <div class="small_title"><u>金巴崙長老會之誕生</u></div>
          <div class="detail_single">
            <p> 因信主的人數不斷增加，信徒極需牧養栽培，教牧人員卻不足夠。根據當時美國長老會的體制，一個人必須「蒙召、接受大學教育和神學訓練，懂拉丁文和聖經原文，才可受按為牧師」。但可`以接受這種訓練的人，在當時顯然不足夠，因此，一位牧師便建議可按立有教導恩賜的信徒加入事奉。約在一八零二年，按立的問題引起爭論。當時美國長老會的金巴崙區會按立了幾位牧師，其中包括伊榮牧師（Rev. Finis Ewing）和金牧師（Rev. Samuel King）。在肯塔基眾會於一八零四年的會議上，會議的成員開始質詢金巴崙區會所按立的牧師未符合美國長老會規定的資格，其中問題包括受按並未完全接納韋斯敏士特信條（Westminster Confession）有關神預定某些人得救的看法，他們認為每一個人都可以因信蒙神救贖而得新生命。到一八零五年十一月肯塔基眾區會會議，議決金巴崙區會按立牧師一事是不符合長老會憲制規條的，要求他們對最近受按者重新考慮，受按者必須完全接納韋斯敏士特信條。當金巴崙區會三位資深的牧師，包括麥嘉道牧師（Rev. Samuel McAdow），均不接受眾區會的決定。一八零六年十月，肯塔基眾會下令解散金巴崙區會，其會員改隸屬特蘭斯維亞區會（Transylvania Presbytery）。 </p> <p>
            當時金巴崙區會的牧師組成一個議會（Council），繼續尋求與美國長老會和解之路。自一八零六年至一八一零年，幾次與總齡會和肯塔基眾區會和解的努力均失敗，其中原因包括他們堅持兩個原則： </p> <p>1. 美國長老會必須接納所有金巴崙區會的牧師和認可宣教師，並要肯定金巴崙區會的法定權利. </p>
             <p>2. 除了含有宿命論意味的信條外，他們接納長老會的信條。實際上，他們的意見從未有機會在總議會上提出過。 </p> <p>在和解的努力失敗後，於一八一零年二月三日，金牧師和伊榮拜會一同往訪麥嘉道牧師田納西州的家，他們三人共同組成一個區會，乃是金巴崙區會。他們最初的原意並不是要成立另一個字派，只是與美國長老會復合的努力失敗後，才會另組區會。於一八一三年，他們再組成另外二個區會：盧根區會（Logan Presbytery）和艾爾其區會（Elk Presbytery）。到一八二九年，正式成立金巴崙長老會總議會。 </p>
            <p>本會早期由美國長老會分出來，教牧人員的資格問題只是事件的起因，在教義上的分歧也是原因之一。當時金巴崙區會的牧師對韋斯敏士特信條有關上帝揀選及棄絕的教義，有若干的保留，使當時的情況更形複雜 。</p>
</div>
          <div class="small_title"><u>復合運動</u></div>
          <div class="detail_single">
           一九零六年，金巴崙長老會的總議會正式通過與美國長老會復合成為一個宗派。當時本會各教會的意見未能達到一致，故此，仍有部份教會拒絕歸回美國長老會。根據統計，在未復合前，本會共有2,869間教會，會友共185,212人。一九零七年，本會1,216間教會，包括80,522位會友加入復合運動，歸入美國長老會。至一九一六年，復合運動已大致過去，留下來的金巴崙長老會共有1,446間，會友共63,735人。當時只有二位宣教士拒絕復合。他們是荷拔女士（Miss Sallie Herbert）和本會華南區工作的創辦人甘成國牧師。<br/>
           <br/>甘成國牧師當時表示：「我在金巴崙長老會認識基督，受洗加入金巴崙長老會，在金巴崙長老會主日學裏得栽培，在金巴崙大學受造就。就讓別人去吧，我決定留下。」
