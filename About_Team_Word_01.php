            <td valign="top" class="section">各位親愛的家長: <br />
                <p>首先，在新一年祝願各位家長新年進步、身心健康! </p>
              <p> 作為教育工作者，我們的工作除了要讓學生獲取書本上的知識之外，更重要的是要教導孩
                子走當行的道路，並學會「施比受更為有福」的道理。我經常提醒同學，我們學校的校訓：「非
                以役人乃役於人」，其意思是我們並不是要受人的服侍，乃是要服侍人，並且要捨命作多人的
                贖價。我堅信生命教育比只獲取知識更為重要， 同學要學會如何付出，才會學懂如何珍惜和
                感恩。 </p>
              <p> 在這個聖誕節，我收到一位舊校家長送給本校學生六百多支朱古力棒，我除了向她表示謝
                意外，還問她花了多少天去做這六百多支朱古力棒?  她說她與兩位子女共花了三晚時間才完
                成。另外，她亦購買了過百份文具，用作獎勵及鼓勵學業成績有進步的同學。雖然我這位家長
                朋友並不富裕，但她卻非常慷慨地為同學送上祝福。她很希望子女學會如何為他人付出，珍惜
                自己所擁有的，並希望小朋友從中學習「施比受更為有福」這個道理。 </p>
              <p> 以下有段對話內容希望與大家分享及思考:</p>
              <table width="98%" border="0" cellpadding="10" cellspacing="1" bgcolor="#FCEEDC">
                  <tr>
                    <td bgcolor="#FEFAF5">當你和子女一起看新聞報道時，他們看到那些瘦骨如柴的孩子時，他們可能會問：「為
                      甚麼那些孩子會那麼瘦？」你會怎樣回
                      你或者會說:「因為他們沒有飯吃。」
                      子女也許會很好奇，想知道他們為甚麼沒有飯吃，善良的孩子或許會說:「那我可以把
                      我的飯分給他們吃嗎?」作為父母，你又會怎樣回答? </td>
                  </tr>
                </table>
              <p> 許多父母雖然對孩子的善心感到欣慰，但他們大概可能說:「你年紀還小，你現在要先吃
                飽，其他的事待長大後再說。」父母們大概都是這樣教導子女的。但是，這樣的孩子長大以後，
                他會怎樣呢？他會否不再關心跟自己利益無關的事呢？</p>
              <p> 其實，我們應該教導孩子多關心別人，關懷世界，不用計較所付出的是多或少。雖然一個
                兒童的能力有限，但當我們每人都能付出一點的力量，再把力量聚集起來，就可以幫助更多的
                人，這就是「以生命影響生命」的真義。</p>
              <p> 最後，農曆新年快到了，在此祝福大家主恩常在、新春快樂、身體健康!</p>
