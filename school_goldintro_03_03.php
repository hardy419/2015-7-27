		 <div class="text_title">中國華南區的宣教事工- 發展階段</div>
<div class="text_line"></div><div class="detail_single">
  <div align="center"><img src="images/gold_05.jpg" class="allimg_border"/></div>
</div><div class="small_title"><u>排外浪潮</u></div>
<div class="detail_single">正當教會事工不斷拓展之際，中國正掀起一片排外運動。飽受外國勢力蹂躪的國土，使中國人在憤怒下推動外國勢力的運動。杯葛日本貨、英國貨，反日、反外國的運動接二連三地舉行，基督教正帶著濃厚的外國色彩，反基督教的運動也自然隨之而進行。</div>
<div class="detail_single">甘牧師於一九二六年的通訊中說：</div>
<div class="detail_single">今年反外、反基督教的運動特別多，這些運動已直接影響整個社會，無論在政治、商業、社會和宗教各方面。整個排外浪潮在五、六月，因幾位學生在上海被警察打死而達高潮。……美國大使館亦勸告所有美國人離開中國。六月二十三日在廣州市一次反外遊行，有好幾個學生也被殺……目前大部分的外國宣教士已離開……。這種情形到一九二九年才較平息。</div>
<div class="small_title"><u>一九三零年後的發展</u></div>
<div class="detail_single">隨著內亂，政治不穩定和排外潮而來的，是全球性經濟不景氣。但本會的各教會的奉獻卻並未因此而減少。</div>
<div class="detail_single">我們的區會要努力自立。本會的同工深明個人的責任是要建立一間經濟上自立的堂會。</div>
<div class="detail_single">廣大堂約於一九三零年經濟自立。當時教會已穩步發展，主日學生有二百人，婦女會和青年團契組織健全。</div>
<div class="detail_single">石灣堂和沙溪堂都設有診所為鄉民服務。當時中國的農民大多生活貧困，國家醫療服務又差，石灣堂和沙溪堂的同工們藉探訪病人，向他們傳福音。在一年內，這兩間診所共為六千五百人提供醫療服務。</div>
<div class="small_title"><u>甘成國牧師去世</u></div>
<div class="detail_single">本會在中國的工場剛從內亂後穩定下來，在中國這片廣大的土地上仍有無數人未得聞福音，中國，仍在四分五裂的動盪下掙扎，神在這時候帶走衪的工人―甘成國牧師。</div>
<div class="detail_single">從甘牧師次子甘耀敬於一九三七年的書信，我們略知他當時的情形。自從我兩年回國後，父親的健康便漸壞。他以往很少生病，但現在卻大不如前了。每次他往不同的堂會工作後回來時都甚疲乏……去年八月，他往中山市的教會回來後心藏便一直有問題。他的心跳不規則，胃口差，行動不便，常常難以入睡。醫生只能給他一個忠告：休息，不可工作。我也曾想過希望總會准辦他退休，但我想，爸爸一定會說：若神仍讓我事奉，我不會退休……在他離世前一週，他已病倒臥牀，我們一直在他身邊照顧他。</div>
<div class="detail_single">甘牧師於一九三七年二月十八日下午十一時三十分息下他在世的勞苦，與榮耀的主相見。神帶走衪的工人，神繼續衪的工作。</div>
<div class="detail_single">甘牧師回國後不久與何慕慈女士成婚，育有二男五女：耀蘇、耀敬、耀道、耀貞、耀芳和耀美（於七歲時去世）。因對本會創始者之欽佩，甘牧師的三個兒子均以三位創始者Finis Ewing, Samuel King和Samuel McAdow命名。甘牧師後來獲本會神學院頒授神學博士銜。</div>
<div class="detail_single">以下是甘牧師的家人、同工、友人對他的悼念：</div>
<div class="detail_single">父親和我底「父子緣」不過只是二十六年的期間，在這個時間的當中，父親底生平事跡，還是在最後那十餘年才能認識清楚，……</div>
<div class="detail_single">我們中國有句「嚴父慈母」的諺語，可是父親對他的兒女，可不像其他父親這麼「嚴」了，雖然我在做錯事的時候，他很嚴厲的教責我們，可是他底慈和的心腸，是他兒女們一生不能忘記的……</div>
<div class="detail_single">父親不只是對家庭如是，他對朋友的恩愛，也是這樣的和藹與恩厚，他一生的朋友很多，不論在中國或在美國，人人對他都有深刻的愛戴─這不是我言過其實―回想我在美洲留學六年，遇見了很多以往熟識父親的朋友，他們都很喜歡和我言談，特別待我，不是因為我有甚麼的長處或學問，卻是因為他們愛戴父親的原故，我所到的美國家庭裏，他們每個都稱讚父親的言行，在種族界限深嚴的美國，對一個中國人這樣的親善，卻不是很常見的事啊！在父親死去那個月裏，本人接到中外朋友寄來的安慰函件，不下三百餘封，在教會的出版物裏，對父親的言行歷史載不勝載，可見各人對父親的真誠表示得無遺了。</div>
<div class="detail_single">父親在教會裏工作有四十多年的歷史，他一生的生活可說是勞苦的，絕不是安閒的。上主給他一個強健身體，能在七十餘年裏未曾受著甚麼疾病的侵擾，他在世最後一二年，為著年事的關係，身心漸漸的退化―這是任何個人必經的地步―但他卻繼續為主工作，直到最終的時期，上主要他休息了才肯把世上的工作放下去，他―在我們眼光裏―不算是世間所謂的偉人，也未有幹著甚麼「驚天動地」的事迹，他不過是世界上一個傳道牧師，犧牲了一切，為著上主天國的實現，在社會人羣謀進展。他，好像保羅先生所說的，己戰了善戰，已行盡奔走的路，已遵守了誡命……甘耀敬，兒子</div>
<div class="detail_single">甘牧師一生無時不為吾主真理奮鬥，鞠躬盡瘁，不避艱困，其志向之堅定，傳教之奮勇，魄力之偉大，實為教會中不可多得之基督精兵，今竟奉召回歸天國，誠為南中國教會之空前損失，援筆書此，不覺黯然。廣大自理堂同人</div>
<div class="detail_single">世界上上有一個人能像甘成國牧師感動我祝福我這樣多，他是一個謙遜、忠心、勸勉、正直和明察的人，我們和他同事三十年，永遠不會忘記這個快樂的印象。婦女海外宣教部長奇麗夫人（Mrs. Johnie Massey Clay）
我永遠不會忘記和甘成國牧師在中國作最後相見的一次，當我們完成了傳道工作……約甘牧師到輪船餐室食午飯，船將啟行回美的時候，我挽住手送他登岸，我們的眼淚禁不住流出，他把我的手緊握說：「告訴美國的弟兄姊姊們，我為著播傳福音，死在我的故土中國。」總會幹事福牧師</div>
<div class="detail_single">甘牧師離世時，本會在廣東省共設立十一個堂會和基址，會友一千五百名，教牧同工、學校教師共五十人。</div>
