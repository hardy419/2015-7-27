		<div class="text_title">金巴崙長老會港澳的宣教事工- 長洲堂</div>
<div class="text_line"></div><div class="detail_single"><div class="small_title"><div class="detail_single">
  <div align="center"><img src="images/gold_010.jpg" class="allimg_border"/></div>
</div><div class="small_title"><u>海傍道鋪位</u></div>
<div class="detail_single">長洲是本會在香港開展工作的第一個據點。長洲當時是個漁村，島上居民大多以捕魚成生，甘牧師在這小島上展開工作，對當時的勞苦大眾傳講一個賜恩典、平安的福音。</div>
<div class="detail_single">一九五二年鞦，在海傍道租一個地下鋪位作聚會處，並開設啟蒙班，使島上居民有機會接受教育。當時的負責人是北角堂黃景褆長老之先父黃露民牧師，並有建道神學院之學生陳淑貞事工。當時不少會友亦由中國抵達香港，一同參與長洲堂的事奉；他們是甘耀穌姊妹、高美德姊妹、謝顯明弟兄、許詔弟兄和文科長老等。</div>
<div class="detail_single">初期的工作對象主要為漁民、學生及其家人。當時大批難民來港，其中定居離島的以水上人為多。向水上人傳福音有一個特色：家中長輩信主，則全家信主；又當全家出海作業時，教會便一下子少了聚會的人，但凡節期，教會中卻又見人頭湧湧。</div>
<div class="detail_single">本會的識字班只辦至四年級，乃長洲島上第一間義學，後基於政府教育制度的改善，學校於一九六四年結束。</div>
<div class="small_title"><u>發展期</u></div>
<div class="detail_single">海傍道的會址對海傍，同時亦鄰近熟食市場，堂址門口經常堆放熟食檔之雜物，主日聚會時亦不時受到大鑼大鼓「節日慶典」之騷擾。但對長洲堂的弟兄姊妹而言，這一切的「配樂」，都已成為教會生活中只可成追憶的一部分。</div>
<div class="detail_single">一九五六年於山頂29號購得一別墅作傳道人宿舍和識字班之用。這幢別墅面積頗大，面向海洋，四週環境寧靜優美，於暑假期間亦作青年夏令會退修之用。</div>
<div class="detail_single">七零年代的青年事工發展迅速，當時的青年人亦有部分曾在教會的識字班就讀。教會注重關顧和教導事工，會友間關係密切，教會事工亦穩步發展。</div>
<div class="small_title"><u>處於變動中的長洲堂</u></div>
<div class="detail_single">七零年或至八零年中，因著堂址的遷移和傳道同工的變動，堂會的事工受到不少的影響，這個改變給弟兄姊妹們帶來沖激也帶來激勵。陳淑貞姑娘於事奉二十年後，因健康緣故於一九七四年離職。在以後十多年內，共有三位同工在長洲堂相繼續事奉，其間亦有缺乏傳道同工的時候，加上舊址亦因屋主收樓而須要遷出，有一段時間借建道神學院的地方暫時聚會，堂會的事工在不大穩定下發展。在這段期間內，本會各堂會的同工均分擔長洲堂主日的講台事奉，另外九龍堂的會友亦積極參與事奉。其中黃華德，任艷玲夫婦於週末往長洲堂事奉，為時兩年；李碧紅姊妹亦於主日協助司琴的工作。長洲堂的弟兄姊妹在這段時間內，充分表現了對教會的關心，他們留守和看守，教會的事工才得以繼續下去。</div>
<div class="small_title"><u>新的裡程</u></div>
<div class="detail_single">堂會終於一九八三年初於北社安榮中心另購新址，並於十月十六日舉行獻堂禮。當時堂會亦有不少變動，會友中不少人因婚姻或工作已移居香港、九龍居住，會友中除少數年長者外，多為表年人，兒童工作亦隨著傳道同工之到任而展開。在以後十年內，堂會的事工蒙神帶領，陳萍仙女士、章永利先生和黃月華女士先後任堂會同工，並得建道神學院之甘汝誠先生和曾景雯女士負責教導事工和擔任堂會顧問，張梁笑媚女士協助教導和訓練的工作，並有建道神學院之同學實習，他們不論短期或長期的事奉，均成美好的見證。</div></div>
