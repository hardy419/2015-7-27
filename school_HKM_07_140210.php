<div class="text_line"></div><div class="small_title"><u>新市鎮的挑戰</u></div>
<div class="detail_single">早於八零年初，新市鎮的迅速發展已向我們展示一個服事的機會，區會因而展開向政府申辦小學及幼稚園的計劃。到八八年底，北角堂和九龍堂均有決心發展新的事工，而區會亦決定不單單等候申辦學校的回音，故開始呼召弟兄姊妹回應新市鎮服事的異象，亦積極找尋合適的地方開始工作。當時回應這異象而加入植堂核心小組的計有：北角堂的游玉蘭姑娘、舉正文先生和關正江弟兄，九龍堂的鄭炳光長老一家和鄧耀祖夫婦，及其後加入道顯堂的韓耀宗弟兄。</div>
<div class="detail_single">沙田堂由一羣「沙田友」於家庭小組聚會開始，首次主日崇拜於一九八九年三月二十六日（復活節主日）假沙田大會堂舉行，約有三十人參加，赴會者多數居住於沙田區。同年四月，以九十萬購得大圍田心村村屋之地下，作為教會聚會地方，並於七月二日舉行獻堂禮。</div>
<div class="small_title"><u>居於羣邨之心─田心村</u></div>
<div class="detail_single">是神把田心村這個地區放於早期植堂小組的心中，在以後事奉的經歷中，也漸漸看到堂會座落之位置，正好給予弟兄姊妹機會，接觸居住於鄰近的人羣。</div>
<div class="detail_single">開堂初期的重點在於接觸和牧養以家庭為單位的成員，年輕的夫婦和幼童是最大的對象。教會也透過不同的聚會：兒童天地、暑期聖經班和婦女小組等，接觸不少鄰舍。當時牧養的重點是敬拜、團契和查經，牧養的模式是建立一個家庭化的教會模式。在立堂三年間，崇拜人數已快五十人，堂會亦因不同事工的發展而感到人力和地方的不足夠。</div>
<div class="detail_single">自九一年開始於暑假開辦英文班及英語班，這英文學習活動成了堂會恆常的事工之一，也因此而接觸不少少年人。本會宣教士白蘭西小組於九零年到香港事奉，她在沙田堂的參與漸多，於週六開辦英語會話班，並協助團奏工作。她於田心村居住的單位亦開放作團契和兒童崇拜的地點。</div>
<div class="detail_single">到九四年初沙田堂址已不夠使用，一方面是聚會人數近六十人，而小組聚會可使用的地方不多。神奇妙地預備鄰近的一個單位，區會遂購買此單位並發展為「學生發展中心」，於一九九五年二月正式投入服務，服務範圍包括小學生課餘托管和補習班。沙田堂是典型的家庭教會，三代同堂的家庭成員一同敬拜神，青少年工作的發展也特別蒙神恩待。游玉蘭姑娘因家庭責任於九四年一月離職，一家人仍留在堂會事奉。</div>
<div class="detail_single">城門河堤盡是高聳筆直的屋邨，住著千萬戶人家，這是神要我們服事之地。</div>
