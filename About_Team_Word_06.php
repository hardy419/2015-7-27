                <p>愉快的暑假過去了，歡迎各家長與子女再次返到「基愛大家庭」。我是基愛小學陳嘉文校長，感
                  謝主引領我到基愛小學，能擔任這所小學校長是我的福氣，基愛小學是我實踐教育理想的好地方，因
                  為天父一直引導和保守，在過去兩任校長---胡家霖校長和郭妙嫻校長的帶領下，為基愛小學建立了優
                  秀的文化和根基，令我能夠與一群委身教育的教職員全心全意為學校奉獻，與家長一起陪伴著孩子愉
                  快地在愛裏成長。</p>
              <p>我從事教育十多年，之前在另一所基督教小學服務超過十四年，過去曾為教育局課程發展處拍攝
                教育電視，影片題目為「提問技巧在小學數學教學中的效能」，大家可能在互聯網或電視台曾經見過
                我;  我亦經常為香港電台擔任電視及電台節目嘉賓主持。我熱愛教育工作，我視學生為自己的子女，
                深信天父創造每一位孩子都是獨特而唯一，每一位都是寶貴和美麗。我盼望所有學生都喜歡上學和熱
                愛閱讀，有強烈的求知慾、自學能力、溝通能力和創意能力，他們不怕面對困難，就算處身於逆境也
                勇於面對、不輕易放棄。每位同學都要有遠大的眼光和理想，有接受新挑戰的意志。我在今年七月的
                結業禮中，曾與所有同學分享過一個「海鷗和麻雀」的故事，現在也希望與家長一同分享。
