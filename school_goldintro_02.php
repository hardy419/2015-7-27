<div class="detail_single">
  <p>本會在一八九七年總議會上，三個人嚴肅地立於台前被差派往中國傳道，他們是盧根醫生（Dr. O.T. Logan）、華爾嘉牧師（Rev. H.L. Walker）和彼斯頓牧師（Rev. T.J. Preston）。本會在日本和墨西哥的宣教工場已開展，現在是往中國去的時候了。</p>
  <p>總逸會在數年前已計劃開展大國的工場，但經濟的負擔與被差派的對象都是要考慮的問題。一八九六年，盧根醫生表示願意往中國傳道，神又奇妙乎感動費文先生（Mr. G.W. Freeman）奉獻一千美元支持盧根醫生未完的學業和中國首年的費用。本會就是這樣踏上了開展中國工場的第一步。</p>
  <p>盧根醫生夫婦和彼斯頓牧師於一八九年九月起行，他們翌年聖誕日便是在湖南省的Chang-the開展工作。一八九九年，總議會繼續差派甘成國牧師（Rev. Gam Sing-Quah）和基利醫生（Rev. and Dr. William Kelly）往中國傳道，但幾經考慮後，感到甘牧師應往其出生地廣東省傳道，而把他暫時派往西岸於三蕃市的華人教會中牧會。</p>
  <p>正當在湖南的工作開展之際，盧根醫生夫婦卻因健康問題要回國休養，兩年後才返回。留下來的同工辛苦耕耘，直至一九零零年拳匪之亂，湖南省的工作被迫停頓，一年才得繼續開展。基利醫生在上海與海外基督徒宣教會（Foreign Christian Missionary Society）的一位宣教士結婚。但基利太太卻於婚後數月病逝，安葬於湖南。拳匪之亂後，他們繼續開展工作。在以後數年，不斷有宣教士加入中國的工場。一九零三年十月，曾健斯牧師師母（Rev. and Mrs. George F. Jenkins）和博斯頓牧師（Rev. I.G. Boydstun）加入。</p>
  <p>一九零五年，碧姬利小姐（Miss May Beekley）和馬田小姐（Miss Mable Martin）亦加入中國的工場。基利醫生約於一九零三年辭職回國。</p>
  <p>本會在中國的宣教工作集中於醫療、佈道、教育這三方面。本會的醫院不單提供醫療服務，並向病人傳福音和辦學。婦女的工作進展頗理想，但其文盲現象則令宣教士們頭痛。美國的教會對中國工場大力支持，在一次由主日學推動的奉獻中，為興建一個福音據點：包括醫院和教會，籌得2,706美元。在中國工場開展七年後，本會共有六位宣教士在中國事奉，有一間醫院，在一九零四年內為6,000人提供門診服務， 137人曾住院治療，一間名為「約翰‧米勒學校」（The John Miller School）的男校，一間女校，在Changteh和Taoyuen有二間教會，共有190 受洗歸主。</p>
  <p>一九零六年，金巴崙長老會的總議會通過與美國長老會復合成為一個宗派，雖然當時仍有約一半教會拒絕復合，但在日本、墨西哥和中國湖南省的宣教事工從此便由美國長老會繼續。盧根醫生和彼斯頓牧師仍留在湖南省的工場事奉。</p>
  <p>本會在湖南省的工作告一段落，但神的工作永不停頓。</p>
