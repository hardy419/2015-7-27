		<div class="text_title">金巴崙長老會港澳的宣教事工- 九龍堂</div>
          <div class="back_page"><a href="school_goldintro.php" target="_self">返回上一頁>></a></div>
<div class="text_line"></div><div class="detail_single"><div class="detail_single">
  <div align="center"><img src="images/gold_011.jpg" class="allimg_border"/></div>
</div><div class="small_title"><u>石硤尾天台年代</u></div>
<div class="detail_single">本會開展的第四個基址亦以教育開始。
<div class="detail_single">一九五八年秋獲政府批准於石硤尾十八座天台開辦金巴崙小學和兒童會。當時上午校學費三元，下午兒童會學費一元五角，不少貧苦家庭的子弟因而有機會入學。甘耀道牧師任小學校長，馮淑媚女士和黎恩先生分別擔任訓導主任和校務主任，而傳道同工則兼教聖經科。同年十一月三日下午三時，在校址舉行首次主日崇拜。當時聚會約有六十多人，參加者除附近居民外，亦有不少是昔日本會在中山教會的會友，故操中山口音者不少。到一九五九年八月北角堂成立後，九龍堂的主日崇拜便改為下午二時，以方便教牧同工往返參加兩堂的崇拜。</div>
<div class="detail_single">五零年至六零年代，是大批難民從中國大陸湧至的年代。當時大部分人生活窘迫，九年免費教育仍未實施，金巴崙小學實在能回應當時的需要。美國的教會亦給予醫療、經濟上的捐助；學校不時分派維他命丸、救濟物品給需要的學生。由於社會的改變，九年免稅教育的實施，金巴崙小學亦於一九七四年結束。</div>
<div class="small_title"><u>發展期</u></div>
<div class="detail_single">於天台學校聚會的時代只屬短期，教會不久於青山道道六十號二樓購得兩個單位作新堂址，並於一九六二年四月二十九日舉行新堂奉獻禮，由美國總會幹事布賴信博士（Dr. David O. Bryson）主禮。新堂會址面積一千二百呎，可容納一百五十餘眾。</div>
<div class="detail_single">當時的同工除黃慕聖先生外，於一九六二年夏，聘請梁真光牧師為主任牧師。一九六四年，總會差派美國華僑胡國光牧師東來協助本會的青年工作。當會的事工於六零年代開始漸上軌道，一方面按立溫天羽弟兄為長老並按立數位執事，以致堂會工作有教大的發展；另一方面因同工配搭事奉，加上梁牧師有音樂的恩賜，大力推廣聖樂，故當時亦是詩班陣容最鼎盛的時代。當時教會的主力軍是青年人，他們負起主日學、任團契導師的責任，亦經常到靈實醫院和廣華醫院傳福音。</div>
<div class="detail_single">胡國光牧師於一九六七年與本會北角堂同工范綺芳姑娘成婚並於七月返美。梁真光牧師於九龍堂事奉七年後，於一九六九年離職，同年八月與家人移居美國羅省。</div>
<div class="small_title"><u>另一個躍進期</u></div>
<div class="detail_single">七零年代是一個躍進期；一方面堂會向「自立、自養、自傅」之道，另一方面，教會各方面的事工都穩步發展，蒙神保守。
<div class="detail_single">七零年初，聘請曹恩保牧師炭九龍堂主任兼責北角堂的工作。曹牧師雖於七一年底離職，在本堂事奉日子不久，但為人和藹可親，活力充沛，具牧者心懷，相信會友們印象深刻。堂會於七三年決定實行經濟自立，不再倚靠差會之支持。當時會眾約有百多人，在一連串的鼓勵和認捐行動後，於七四年底每月奉獻皆超過五千元，足可步上自立之途。七四年選出鄭炳光及黎恩為長老，堂會的理事會遂於七五年一月正式成立，由黃慕聖先生任主席，堂會正式走上自立、自養之路。</div>
<div class="detail_single">這個階段也是堂會迅速發展的階段，每年都有十至二十多人加入教會，堂會各部門事工均有很好的發展。於七六年組成擴堂小組，以物色較大的堂址，並舉行義賣、步行、募捐運動和於大會堂舉行音樂會，以籌款擴堂。擴堂工作幾經波折，最後以125萬購得順利大廈共五個單位，面積約有三千呎作為新堂。八三年五月八日於新堂舉行獻堂禮，並請得前區會總幹事甘耀道牧師、師母自美返港主持奉獻禮。</div>
<div class="detail_single">黃慕聖先生於一九七八年十月往美國總會接受按立牧師聖職。堂會的同工於八零年代有不少變動，分別有張略先生，何國偉先生和何威達先生於不同時間內加入，教會的事工面對八零年代的挑戰，也開始發展不同的牧養模式。</div>
<div class="small_title"><u>轉變與挑戰</u></div>
<div class="detail_single">八零年代到九零年代，香港社會急劇變化，加上九七問題所引起的動盪，教會牧養的模式，信徒面對生活的壓力和挑戰也不同。堂會開始發展和建立以小組為導向的教會並加強裝備信徒領袖。這十年來同工的變動也不少，何威達弟兄於八九年九月離職往新加坡牧會，張略先生於一九九一年二月二十四日受按牧職，黃慕聖牧師於九三年八月調往北角堂，而林慧靈姑娘亦於同期加入為同工，何國偉先生於九三年任堂主任，至九六年初調往寶林堂發展中心之工作。</div>
<div class="detail_single">鄭炳光長老一家和幾位會友回應沙田基址的異象，於八九年加入沙田堂的開荒工作。蔡維章長老亦於九一年夏進入神學院，裝備事奉神。</div>
<div class="detail_single">三十八年的歷史，三十八年的恩典，堂會要扎根基督，邁步向前。</div></div>    
