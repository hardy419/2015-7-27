		 <div class="text_title">中國華南區的宣教事工- 戰火下的國土</div>
          <div class="back_page"><a href="school_goldintro.php" target="_self">返回上一頁>></a></div>
<div class="text_line"></div><div class="detail_single">
  <div align="center"><img src="images/gold_06.jpg" class="allimg_border"/></div>
</div><div class="small_title"><u>神的工人―甘耀敬</u></div>
<div class="detail_single">甘耀敬是甘成國牧師的次子，他於一九二九年赴本會在美國的伯舟利大學（Bethel College）進修，隨後再於雲德堡大學（Vanderbilt University）獲碩士學位。一九三五年，他接獲家書：我盼望你趕快回國協助教會工作…..我相信我能教導你的經驗，是你在美國怎樣也學不到的。
<div class="detail_single">甘耀敬回國後，便立刻協助其父在教會中事奉，他去信美國友人說：試想想你生活於一個有四千年歷史的故舊國家中，而中國正在不斷存求改革。我認為若可以用我個人徵小的力量令中國更強大，這實在是我極大的權利……在人們心中，他們都明白人活著不是單靠食物……他們心中都渴望神的話語，願意按主耶穌的教導去生活。當甘成國牧師去世後，本會華南區的工作便落在甘耀敬身上。</div>
<div class="small_title"><u>日軍侵佔</u></div>
<div class="detail_single">隨著內亂天來的，是國土被日軍佔領。一九三七年七月七日發生「蘆溝橋事變」，抗日戰爭正式開始，全國各地都在烽煙之下，廣州坊於一九三八年淪陷。甘耀敬曾這樣報道：這年頭的生活簡直像一場悲慘難忘的噩夢，我們無時無刻不是生活在恐懼下……三八年五月二十八日，日軍開始轟炸廣州市，八月八日廣大堂被炸毀，我們只得在附近租借地方聚會。河南堂逃過大難，教會開放收容難民……佛山古洞堂、石灣堂、鷺州堂和沙漖堂都被日軍佔領，但教會建築物則無損，只是可灣堂被日軍搶掠一空。我們只得結束鷺州堂……目前只有沙溪堂、石岐堂、大環堂仍在中國軍隊佔領下。沙溪堂目前情況最好，會友增加，教會事工運作正常……因抵受不住不停的轟炸，石岐堂已遷到澳門聚會。
三八年十月，廣州市淪陷，教會被逼遷到香港暫時聚會。到四零年初的情形，甘耀敬這樣報道：<br />
沙漖堂的會友因逃避戰火和土匪的搶掠都離開了，教會的突然曾數度遭搶掠並打傷……大環堂最不幸，學校和宿舍都被炸毀了……梁牧師差點連性命也不保。石岐堂曾被侵佔，幸好損失不大。會友們大都離開了，但教會仍有聚會。</div>
<div class="detail_single">一年前我們已在廣州市的女青年會做救濟的工作，國際紅十字會贊助我們的服務，以致我們能為四百位難民提供膳食……我們亦有醫療服務，如預防天花、霍亂的注射等……我們的兒童院目有五十個孩子住院。
甘耀敬於一九四一年向總會報道：<br />
廣大堂在四年內三易其址，雖然會友分散各地，但基本上教會仍如常……在昋港仍有我們的臨時聚會點；河南堂漸上正軌；佛山堂、石灣堂和沙漖堂都穩定。雖然土匪在中山市為患，但石岐堂、大環堂和沙溪堂的工作仍正常，沙溪堂仍是各會中情況最好的。</div>
<div class="detail_single">從我的報告中，你也許以為我們正陷於失望和悲痛中，事實上，苦難只使我們更懂得仰望主耶穌……教會成了叫人得安慰和盼望的地方，我們的服事感動很多人，教會也從未試過有這樣多的機會去宣揚福音。我們所經歷的不是苦難，而是盼望，何處有苦難，何處便有喜樂和盼望。</div>
<div class="small_title"><u>歸去</u></div>
<div class="detail_single">正當中國陷於內亂與外敵的形勢下，甘成國牧師的另一個兒子甘耀道卻於一九三八年七月登上奇夫連總統號輪船，遠赴美國田納西州，踏其先父及兄長後塵，於伯特利大學深造。</div>
<div class="detail_single">我盼望在大學裏主修教育，又修讀有關電台傳播的科目，因為我希望以後可以在中國成立金巴崙長老會電台，請你為我達到此目標而禱告。</div>
<div class="detail_single">甘耀道於一九四一年畢業，主修數學和科學。於一九四三年，他於Goerge Peaboby College獲碩士學位，主修公共衛生和教育。去國五年，甘耀道卻始終未忘神州大地。他整頓行裝，奔向這個他成長的地方。</div>
<div class="detail_single">你們都知道在短期內我便會返回中國，其實先我踏足美國那天開始，我無時不在準備自己返回國土。我的回歸縱使並不如先父一樣，但我們抱著同一的熱切。中國極需各種援助，若需要宣講，我便宣講；若要教導，我便教導，若需要醫療救助，我便加以援手。我的工作便是回應中國目前所需。
一九四三年夏，甘耀道啟程回國，但由於第二次大戰關係，他幾經轉折，到一九四四年才抵達國門。他隨即在國際救援會（International Relief Commission）負責救濟工作。</div>
<div class="detail_single">此刻的中國飽受戰火之苦，民不聊生。</div>
<div class="detail_single">饑荒和疾病四處可見。白米貴至三十六美元一磅，女童則賣六美元一磅、男童八美元一磅，父母為了生存或養活一家真是會這樣做。
中國在戰火聲中靜待黎明的到來。</div>
<div class="small_title"><u>大戰過後</u></div>
<div class="detail_single">戰爭於一九四五年八月結束，為中國帶來短暫的和平，本會與美國總會的聯絡在中斷後再繼續。本會各教會在戰火過後，面對艱辛的工作。</div>
<div class="detail_single">廣大堂已被炸毀至片瓦不留；他們連磚瓦也拿走了，因他們說這是「敵人的物業」。教會其他的物業雖被炸，但幸好仍得保存。我們希望神再賜我們另一個地方，以致可以重建廣大堂。</div>
<div class="detail_single">早於一九四四年，本會總議會已通過設立甘成國紀念基（Gam Sing Quah Memorial Fund）以重建本會在中國的堂址。這個行動各教會大力支持，到一九四七年甘耀敬牧師訪美時，已共籌得三萬五千美元。本會在戰爭期間被毀的教堂也逐一重建；廣大堂部分重建，其他教會亦完成重修和重建的工作，本會的兒童院也在甘耀道的帶領下有很大的發展。</div>
<div class="detail_single">戰後的中國仍在掙扎求存，很多人仍喪失家園，饑荒的情形則四處可見。甘耀道參與的救援工作提供膳食和醫療的援助，並借貸與農民和難民，以重建家園。</div>
