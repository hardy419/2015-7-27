            <dt><strong>小學級</strong></dt>
            <dd class="txt">小學團員獎勵計劃分為四個等級，各級的獎勵準則如下：<br />
小學基本級 (粉黃色徽章) (累積積點：4)<br />

1. 頒發予獲四個積點的團員。<br />

2. 其中兩個積點必須從參與社會服務取得，另外兩積點可從參與社會服務或培養九個共通能力有關的活動取得。<br />

小學初級 (粉綠色徽章) (累積積點：4+6=10)<br />

1. 完成小學基本級後再獲六個積點的團員。<br />

2. 其中四個積點必須從參與社會服務取得，另外兩個積點可從參與社會服務或培養九個共通能力有關的活動取得。<br />

小學中級 (粉橙色徽章) (累積積點：10+8=18)<br />

1. 完成小學初級後再獲八個積點的團員，其所獲積點必須包括最少一項屬第二類或第三類活動所得。<br />

2. 其中六個積點必須從參與社會服務取得，另外兩個積點可從參與社會服務或培養九個共通能力有關的活動取得。<br />


小學高級 (粉紫色徽章) (累積積點：18+12=30)<br />

1. 完成小學中級後再獲十二個積點，而其所獲積點必須包括從參與以下活動取得：<br />

(i) 兩項屬第二類的活動；或<br />

(ii) 一項屬第三類的活動。<br />

2. 其中八個積點必須從參與社會服務取得，另外四個積點可從參與社會服務或培養九個共通能力有關的活動取得。<br />

3. 最少曾參與三項不同性質的活動／服務。

