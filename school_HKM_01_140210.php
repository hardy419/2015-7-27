</div><div class="small_title"><u>新里程</u></div>
<div class="detail_single">隨著中國政局的改變，大批難民由中國湧入澳門、香港。甘耀敬牧師一家也由廣州到澳門，當時本會亦有不少會友遷居港澳，本會工作因而漸漸開展。</div>
<div class="detail_single">一九五零年三月在下環區租一個單位設立佈道所和識字班，當時服事的對象多為中國遷至澳門之難民、貧困無依之人及漁民。一九五一年十月甘耀敬牧師一家赴美定居，本會的工作遂由區吉璇先生負責。</div>
<div class="small_title"><u>早期的事工</u></div>
<div class="detail_single">甘耀道牧師於一九五二年受按牧職，隨後便協助發展本會港澳區的工作。</div>
<div class="detail_single">一九五二年初，租用河邊新街十號作會址並學校。該址樓高兩層，地方不很大。最初辦學由識字班至小學四年級，提供免費教育予貧困家庭的子女。學校後來獲國際宣明會資助，以致可以開辦完整的六年小學教育，澳明堂的事工也以學校的學生為主。</div>
<div class="small_title"><u>數易堂址</u></div>
<div class="detail_single">河邊新街舊址終因業主收回樓宇重建而結束，於一九七六年遷至媽閣鈄巷聚會。新址樓高三層，除二樓作崇拜之用外，樓下及三樓均用作教室。根據資料，學校於一九七七年末共有學生147人，教職員9人，每月獲宣明會資助約6,700元。學校的發展仍困難重重，一方面由於新均地點較偏僻，交通不便，另一方面宣明會因為經濟收縮而停止津貼，學校頓陷入重重困難中。當時總會之海外宣教部答允繼續作短期的承擔，但到八零年初，學生人數逐漸減少，而亦未有任何長期經濟的資助，學校在八三年七月正式停辦，當時，約只剩下學生30人。</div>
<div class="detail_single">一九八六年五月，遷入爐石塘庇山耶街現址。現址地方寬敞，交通方便，教會亦開始脫離以學生為對象之事奉方式。在過去三十年的辦學過程中，教會接觸過不少學生，目前澳門堂會友中，有不少是教會小學的畢業生。自小學停辦後，澳門堂的事工也走向多元化的發展，舉辦不同的興趣小組以接觸人羣。</div>
<div class="detail_single">堂會近年來雖然缺乏同工，但神興起祂的兒女積極發展教會的工作，並有黃志強牧師、師母和王其昌牧師於不同的時間內，擔任堂會的顧問牧師。</div>
